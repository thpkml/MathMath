    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        //check if empty
        int l10 = jTextField10.getText().length();
        int l11 = jTextField11.getText().length();
        int l12 = jTextField12.getText().length();
        if (l10 == 0 || l11 == 0 || l12 == 0) {
            jLabel114.setText("Please do not leave the boxes empty !");
        } else {
            //empty the warning text
            jLabel114.setText("");
            int d1 = Integer.parseInt(jLabel53.getText());
            int d2 = Integer.parseInt(jLabel55.getText());
            int d3 = Integer.parseInt(jLabel57.getText());
            int d4 = Integer.parseInt(jLabel60.getText());
            int d5 = Integer.parseInt(jLabel62.getText());
            int d6 = Integer.parseInt(jLabel64.getText());

            int div1 = Integer.parseInt(jTextField10.getText());
            int div2 = Integer.parseInt(jTextField11.getText());
            int div3 = Integer.parseInt(jTextField12.getText());

            int rem1 = Integer.parseInt(jTextField15.getText());
            int rem2 = Integer.parseInt(jTextField16.getText());
            int rem3 = Integer.parseInt(jTextField17.getText());

            // Check if you have any lives left
            livesLeft(lives);
            if (lives > 0) {

               // Check if all correct
                    if (d1 / d2 == div1 && d3 / d4 == div2 && d5 / d6 == div3 &&
                            d1 % d2 == rem1 && d3 % d4 == rem2 && d5 % d6 == rem3) {

                            // score 250 per correct answer
                            int level4Score = 750;

                            //bonus score for lives left
                            bonus = lives * 100;
                            setBonus(bonus);

                            // set total score for the game
                            score += bonus + level4Score;
                            setScore(score);

                            //set game score
                            setGameScore(score, bonus);

                            // display "Correct"
                            jLabel78.setText("CORRECT !");
                            jLabel79.setText("CORRECT !");
                            jLabel80.setText("CORRECT !");
                            // hide current level and show next panel
                            level4.setVisible(false);
                            certificate.setVisible(true);

                    } else {
                            // Check answer 1
                            if (d1 / d2 != div1 || d1 % d2 != rem1) {
                                    jLabel78.setText("Try Again");
                                    lives -= 1;
                                    livesLeft(lives);
                            } else { jLabel78.setText("CORRECT !"); } 

                            // Check answer 2
                            if (d3 / d4 != div2 || d3 % d4 != rem2) {
                                    jLabel79.setText("Try Again");
                                    lives -= 1;
                                    livesLeft(lives);
                            } else { jLabel79.setText("CORRECT !"); }

                            // Check answer 3
                            if (d5 / d6 != div3 || d5 % d6 != rem3) {
                                    jLabel80.setText("Try Again");
                                    lives -= 1;
                                    livesLeft(lives);
                            } else { jLabel80.setText("CORRECT !"); }
                    }

            } else {
                    level4.setVisible(false);
                    tryAgain.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton13ActionPerformed

