    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //check if empty
        int l1 = jTextField6.getText().length();
        int l2 = jTextField4.getText().length();
        int l3 = jTextField5.getText().length();
        if (l1 == 0 || l2 == 0 || l3 == 0) {
            jLabel108.setText("Please do not leave the boxes empty !");
        } else {
            //empty the warning text
            jLabel108.setText("");
            //check answers
            int a1 = Integer.parseInt(jLabel11.getText());
            int a2 = Integer.parseInt(jLabel13.getText());
            int sum1 = Integer.parseInt(jTextField6.getText());

            int a3 = Integer.parseInt(jLabel16.getText());
            int a4 = Integer.parseInt(jTextField4.getText());
            int sum2 = Integer.parseInt(jLabel20.getText());

            int a5 = Integer.parseInt(jTextField5.getText());
            int a6 = Integer.parseInt(jLabel23.getText());
            int sum3 = Integer.parseInt(jLabel25.getText());
        
            // check if you have lives left
            livesLeft(lives);
            if (lives > 0) {

                // if all correct
                if (a1 + a2 == sum1 && a3 + a4 == sum2 && a5 + a6 == sum3) {
                    //set score 100 per correct answer
                    score += 300;
                    setScore(score);
                    jLabel69.setText("CORRECT !");
                    jLabel70.setText("CORRECT !");
                    jLabel71.setText("CORRECT !");

                    //set the random numbers for next level (2)
                    //question 1
                    int rand3 = 10 + rand.nextInt(20);
                    jLabel27.setText(String.valueOf(rand3)); //to make the first number bigger
                    jLabel29.setText(String.valueOf(rand.nextInt(10)));

                    //question 2
                    int rand4 = 10 + rand.nextInt(20); //to make the sum high enough
                    jLabel32.setText("" + rand4);
                    jLabel36.setText("" + rand.nextInt(10));

                    //question 3
                    jLabel39.setText("" + rand.nextInt(20));
                    jLabel41.setText("" + rand.nextInt(20)); //to make the sum high enough

                    level1.setVisible(false);
                    level2.setVisible(true);
                } else {
                        //check answer 1
                        if (a1 + a2 != sum1) {
                            jLabel69.setText("Try Again");
                            lives -= 1;
                            livesLeft(lives);
                        } else { jLabel69.setText("CORRECT !"); }

                        //check answer 2
                        if (a3 + a4 != sum2) {
                            jLabel70.setText("Try Again");
                            lives -= 1;
                            livesLeft(lives);
                        } else { jLabel70.setText("CORRECT !"); }

                        //check answer 3
                        if (a5 + a6 != sum3) {
                            jLabel71.setText("Try Again");
                            lives -= 1;
                            livesLeft(lives);
                        } else { jLabel71.setText("CORRECT !"); }
                    }
            } else {
                level1.setVisible(false);
                tryAgain.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

